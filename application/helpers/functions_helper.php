///CheckFileExtention
if (!function_exists('CheckFileExtention')) {

    ///NoProfile
    function CheckFileExtention($filename)
    {
        $file_ext = pathinfo($filename, PATHINFO_EXTENSION);

        $ext = strtolower($file_ext);
        $flag = 0;
        switch ($ext) {
            case 'jpg':
                $flag = 1;
                break;
            case 'jpeg':
                $flag = 1;
                break;
            case 'png':
                $flag = 1;
                break;

            default:
                $flag = 0;
                break;
        }

        return $flag;
    }
}


